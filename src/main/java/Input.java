import java.util.Scanner;

/**
 * The type Input.
 */
public class Input {

    /**
     * The Input.
     */
    Scanner input = new Scanner(System.in);

    /**
     * Gets input next int.
     *
     * @return the input next int
     */
    public int getInputNextInt() {
        //Getter for Input
        return input.nextInt();
    }

}
